var day=3;
var dayName;
switch(day){
    case 1:
        dayName='Monday';
        break;
    case 2:
        dayName='Tuesday';
        break;
    case 3:
        dayName='Wednessday';
        break;
    case 4:
        dayName='Thursday';
        break;
    case 5:
        dayName='Saterday';
        break;
    default:
        dayName='Invalid day';
}
console.log(dayName);       //Wednessday